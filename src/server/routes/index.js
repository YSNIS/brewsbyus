const todosController = require('../controllers').todos;

module.exports = (app) => {
  
  // API ROUTES
  app.get('/api', (req, res) => res.status(200).send({
    message: 'Welcome to the Todos API!',
  }));

  app.post('/api/todos', todosController.create);
  app.get('/api/todos', todosController.list);
};